package Assignment;
import javax.swing.*;
import java.awt.*;
import java.awt.event.*;
import java.util.*;
public class RockPaperScissor extends JFrame implements ActionListener{
	static final int ROCK = 0;
	static final int PAPER = 1;
	static final int SCISSOR = 2;
	private JPanel panel;
	private JTextField output, information;
	private JButton rock, paper, scissor;
	public RockPaperScissor() {
		super("가위 바위 보");
		this.setSize(400, 300);
		this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		panel = new JPanel(new GridLayout(0, 3));
		information = new JTextField("아래의 버튼 중에서 하나를 클릭하시오!");
		output = new JTextField(20);
		rock = new JButton("ROCK");
		paper = new JButton("PAPER");
		scissor = new JButton("SCISSOR");
		panel.add(rock);
		panel.add(paper);
		panel.add(scissor);
		rock.addActionListener(this);
		paper.addActionListener(this);
		scissor.addActionListener(this);
		add(information, BorderLayout.NORTH);
		add(panel, BorderLayout.CENTER);
		add(output, BorderLayout.SOUTH);
		this.setVisible(true);
	}
	public void actionPerformed(ActionEvent e) {
		Random random = new Random();
		int computer = random.nextInt(3);
		if(e.getSource()==rock) {
			if(computer==SCISSOR)
				output.setText("사용자 승리");
			else if(computer==ROCK)
				output.setText("비겼음");
			else output.setText("컴퓨터 승리");
		}
		else if(e.getSource()==paper) {
			if(computer==ROCK)
				output.setText("사용자 승리");
			else if(computer==PAPER)
				output.setText("비겼음");
			else output.setText("컴퓨터 승리");
		}
		else {
			if(computer==PAPER)
				output.setText("사용자 승리");
			else if(computer==SCISSOR)
				output.setText("비겼음");
			else output.setText("컴퓨터 승리");
		}
	}
	public static void main(String[] args) {
		RockPaperScissor g = new RockPaperScissor();
	}
}
